package Model;

/**
 *
 * @author LucasCorrea
 */
public abstract class People {
  
private String name;
private int year;

    
public People(String name, int year){
    this.name = name;
    this.year = year;
}

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }
    


    
}
